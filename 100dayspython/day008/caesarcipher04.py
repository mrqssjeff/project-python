alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm',
            'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z',
            'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm',
            'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']


def caesar(direction_h, plain_text, shift_amount):
    cipher_text = ""
    while shift_amount > 26:
        shift_amount %= 26
    if direction_h == 'decode':
        shift_amount *= -1
    for char in plain_text:
        if char in alphabet:
            position = alphabet.index(char)
            new_position = position + shift_amount
            cipher_text += alphabet[new_position]
        else:
            cipher_text += char
    print(f"The encoded text is {cipher_text}")


should_continue = True
while should_continue:
    direction = str(input("Type 'encode' to encrypt, type 'decode' to decrypt: ")).strip().lower()
    text = str(input("Type your message: ")).strip().replace(' ', '').lower()
    shift = int(input("Type the shift number: "))
    caesar(direction_h=direction, plain_text=text, shift_amount=shift)
    response = str(input("Do you want to continue? Type 'yes' or 'no': ")).strip().lower()
    if response == 'no':
        should_continue = False
        print("Goodbye!")
